package Integracion.Empleado;

public class TEmpleado {
	private Integer idEmpleado;
	private Integer telefono;
	private Boolean activo;
	private String nombre;
	private String password;
	private Integer idDepartamento;
	private String tipoEmpleado;
	
	
	public TEmpleado(Integer idEmpleado, Integer telefono, Boolean activo, String nombre, String password) {
		this.idEmpleado = idEmpleado;
		this.telefono = telefono;
		this.activo = activo;
		this.nombre = nombre;
		this.password = password;
	}
	
	public TEmpleado(Integer telefono, Boolean activo, String nombre, String password, Integer idDepartamento) {
		super();
		this.telefono = telefono;
		this.activo = activo;
		this.nombre = nombre;
		this.password = password;
		this.idDepartamento = idDepartamento;
	}
	public TEmpleado(Integer id, Integer telefono, Boolean activo, String nombre, String password, Integer departamento) {
		super();
		this.idEmpleado = id;
		this.telefono = telefono;
		this.activo = activo;
		this.nombre = nombre;
		this.password = password;
		this.idDepartamento = departamento;
	}
	
	public Integer getIdEmpleado() {
		return idEmpleado;
	}
	public void setIdEmpleado(Integer idEmpleado) {
		this.idEmpleado = idEmpleado;
	}
	public Integer getTelefono() {
		return telefono;
	}
	public void setTelefono(Integer telefono) {
		this.telefono = telefono;
	}
	public Boolean getActivo() {
		return activo;
	}
	public boolean isActiva() {
		return activo;
	}
	public Integer getDepartamento(){
		return this.idDepartamento;
	}
	public void setDepartamento(Integer departamento){
		this.idDepartamento = departamento;
	}
	public void setActivo(Boolean activo) {
		this.activo = activo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	public void setTipoEmpleado(String tipo){
		this.tipoEmpleado = tipo;
	}
	public String getTipoEmpleado(){
		return this.tipoEmpleado;
	}
}
